    <h4>What Are You Paying for Now?</h4>
    <p>Even if your mortgage is paid off, there’s a cost to homeownership. There are ongoing expenses to maintain the value of your home – whether you do it yourself or pay someone else. You can expect to pay between 1 and 3 percent of the home’s initial costs for annual maintenance. Owners of a $200,000 house should plan on spending $2,000 – $6,000 per year in upkeep.</p>
    <p>Terence O’Malley, an estate planning lawyer and elder law attorney, calls this expense your invisible mortgage. When discussing it with seniors, he asks, “If you deposited $100,000 into the bank and your banker told you that you have to pay $5,000 a year just to keep the value of your deposit at $100,000, would you take that deal?”</p>
    <p>Now think about other costs of staying in your house – taxes, insurance and house owners association dues. Then consider related expenses such as groceries, entertainment, dining out, membership dues to gyms or the country club. Plus, you have car insurance, maintenance, and gas.</p>
    <h4>What Will You Pay for in the Future?</h4>
    <p>Will your home need any major repairs in the next year or so? Maybe, a new roof, heating or cooling system, or driveway? Those could cost thousands of dollars.</p>
    <p>If you plan to age in place in a house, you need to think about remodeling for safety and convenience. According to the Centers for Disease Control and Prevention, more than one out of four Americans age 65 and older fall each year, and one in five falls cause serious injury. So it might be necessary to modify your home to accommodate your changing needs.</p>
    <p>Small projects, such as adding grab bars in the bathroom, can cost just a few hundred dollars. More extensive renovations can add up. For example, remodeling a bathroom by widening the doorway to be wheelchair accessible, reinforcing walls to support grab bars, putting in a comfort-height toilet, zero-threshold shower and sink you can sit at – could average more than $15,700.</p>
    <p>Also you need to factor in the cost of future health care. You can get an idea of how much different levels of care can cost you now, and a few years from now, at the 2016 Genworth Cost of Care Survey.</p>
    <h4>The Value of Senior Living Communities</h4>
    <p>“People might look at senior housing as expensive,” said Beth Burnham Mace, Chief Economist for the National Investment Center for Seniors Housing & Care (NIC). “But you need to compare those costs to the costs of staying in your own home as you age, as well as weigh them against the value of everything you get in a senior community.”</p>
    <p>“More and more seniors are redefining their financial perspectives,” said O’Malley. “They’re focusing on what converting their illiquid investment – their house – can purchase to make their senior years among the best and happiest times of their lives.”</p>
    <p>O’Malley said the deed to your house won’t pay for maintenance and housekeeping, security, transportation, health care, amenities and hospitality, entertainment and enrichment programs, and a social environment full of potential friends. “A deed won’t create a physically safe environment designed with seniors’ needs in mind, and where there are emergency responders and staff always at the ready.”</p>
    <p>Mace added, “I’ve talked with people who, until they did the math themselves, couldn’t imagine they could afford senior living. Many times it’s comparable, or even better, than staying in your house.”</p>
